my_string = "Hello, Python!"

print("Uppercase:", my_string.upper())
print("Lowercase:", my_string.lower())
print("Title:", my_string.title())
print("Replace 'Python' with 'World':", my_string.replace("Python", "World"))
print("Find 'Python':", my_string.find("Python"))
print("Is it alphabetic?", my_string.isalpha())
print("Is it alphanumeric?", my_string.isalnum())
print("Is it numeric?", my_string.isnumeric())
print("Length of string:", len(my_string))
print("String stripped of spaces:", my_string.strip())
